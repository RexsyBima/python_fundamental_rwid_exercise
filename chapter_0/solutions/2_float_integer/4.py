""" 
Exercise 4: Type Conversion between Float and Integer
1. Create a float variable called num_float and assign it a value.
2. Convert num_float to an integer and print the result.
3. Create an integer variable called num_int and assign it a value.
4. Convert num_int to a float and print the result.
"""


num_float = 3.14
num_int = 42

# Convert float to integer
num_int_from_float = int(num_float)
print(num_int_from_float)

# Convert integer to float
num_float_from_int = float(num_int)
print(num_float_from_int)
